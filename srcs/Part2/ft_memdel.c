#include "libft.h"

void	ft_memdel(void **ap)
{
	size_t i;

	i = 0;
	while (ap[i])
	{
		free(ap[i++]);
	}
	while (i-- != 0)
	{
		ap = NULL;
		ap++;
	}
}
